#Compute the standard deviation of the NumPy array

 #Input: arr = [20, 2, 7, 1, 34]


import numpy as np  # Importing the NumPy library

# Given array
arr = np.array([20, 2, 7, 1, 34])  # Creating a NumPy array with the given values

# Compute the standard deviation
# np.std() calculates the standard deviation of the array
std_value = np.std(arr)

# Output the result
print("Standard deviation of the array:", std_value)  # Printing the standard deviation


#ans Standard deviation of the array: 12.937810560109547



