#2. Declare a square() function with one parameter.Then call the function and pass one number and display the square of that number


# Function to compute the square of a number
def square(x):
    # Returns the square of x
    return x * x


# Calling the square function with a number (5)
result = square(5)


# Displaying the square of the number
print("The square of the number is:", result)


#ans The square of the number is: 25
