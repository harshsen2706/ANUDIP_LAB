#1. Write a Python program to find the number of times 4 appears in the tuple. 

#Input: tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7 ) Output: 3


# Define the tuple
tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7)

# Use the count() method to find the number of occurrences of 4
count_of_4 = tuplex.count(4)

# Output the result
print("Number of times 4 appears in the tuple:", count_of_4)


#ANS Number of times 4 appears in the tuple: 3

