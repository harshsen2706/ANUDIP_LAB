#1. Write a Python program to sum all the items in a list.


# Python program to sum all the items in a list

# Function to calculate the sum of items in a list
def sum_of_list(lst):
    # Using the built-in sum() function to add all the elements of the list
    return sum(lst)

# Example list
my_list = [10, 20, 30, 40]

# Calling the function and storing the result
result = sum_of_list(my_list)

# Printing the result
print("The sum of the list is:", result)


#ans The sum of the list is: 100
 
